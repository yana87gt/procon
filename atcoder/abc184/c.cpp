bool reach(ll a, ll b, ll c, ll d) {
    return (a + b == c + d) || (a - b == c - d) || abs(a-c) + abs(b-d) <= 3;
}
int solve(ll a, ll b, ll c, ll d, bool first) {
    if (a == c && b == d) return 0;
    if (reach(a,b,c,d)) return 1;
    if ((a+b)%2 == (c+d)%2) return 2;
    ll res = 3;
    if (first) {
        int dlt[] = {-3,-2,-1,0,1,2,3};
        for (int dx : dlt) for (int dy : dlt) {
            if (abs(dx) + abs(dy) <= 3) {
                chmin(res, 1 + solve(a+dy, b+dx, c, d, false));
            }
        }
    }
    return res;
}
